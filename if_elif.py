# EXAMPLE 1
x = int(input("x için bir sayı girin: "))
y = int(input("y için bir sayı girin: "))

if x > y:
    print("x y'den büyük")

elif x == y:
    print("x y'ye eşittir")

else:
    print("y x'ten büyük")

